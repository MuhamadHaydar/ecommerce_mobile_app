
import 'package:flutter/material.dart';

class FontConstants {
  static TextStyle k24KurdishBold(double fontSize, Color color) =>
      TextStyle(fontSize: fontSize, color: color, fontFamily: 'k24kurdishbold');

  static TextStyle k24KurdishLight(double fontSize, Color color) =>
      TextStyle(fontSize: fontSize, color: color, fontFamily: 'k24kurdishlight');
}